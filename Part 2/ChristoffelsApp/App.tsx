import React from 'react';
import { NavigationContainer } from '@react-navigation/native';
import { createStackNavigator } from '@react-navigation/stack';
import WelcomeScreen from './screens/WelcomeScreen';
import LoginScreen from './screens/LoginScreen';
import SignUpScreen from './screens/SignUpScreen';
import MenuSelectionScreen from './screens/MenuSelectionScreen';
import MenuDetailsScreen from './screens/MenuDetailsScreen';

const Stack = createStackNavigator();

const App = () => {
  return (
    <NavigationContainer>
      <Stack.Navigator initialRouteName="Welcome">
        <Stack.Screen name="Welcome" component={WelcomeScreen} />
        <Stack.Screen name="Login" component={LoginScreen} />
        <Stack.Screen name="SignUp" component={SignUpScreen} />
        <Stack.Screen name="MenuSelection" component={MenuSelectionScreen} />
        <Stack.Screen name="MenuDetails" component={MenuDetailsScreen} />
      </Stack.Navigator>
    </NavigationContainer>
  );
};

export default App;
